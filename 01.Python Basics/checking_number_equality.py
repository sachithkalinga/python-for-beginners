
# Equality operator..................................

a = 5
print(a==5)

Car = "Tesla"
print(Car == "Tesla")

# Inequality operator................................

a = 5
print(a!=5)

Car = "Tesla"
print(Car != "Tesla")

result = 7 != 10
print(result)

score_one = 100
score_two = 100
is_gap = score_one != score_two

print(is_gap)
